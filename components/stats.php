<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Animate floating orbs
        const orbs = document.querySelectorAll('.neo-float');
        orbs.forEach((orb, index) => {
            orb.style.animationDuration = `${8 + Math.random() * 7}s`;
            orb.style.animationDelay = `${Math.random() * 2}s`;
        });

        // Intersection Observer for stats animation
        const statsObserver = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    // Animate metrics with staggered delay
                    const metrics = document.querySelectorAll('.neo-metric');
                    metrics.forEach((metric, index) => {
                        setTimeout(() => {
                            metric.classList.add('neo-animate');
                        }, 150 * index);
                    });

                    // Animate tags with staggered delay
                    const tags = document.querySelectorAll('.neo-tag');
                    tags.forEach((tag, index) => {
                        setTimeout(() => {
                            tag.classList.add('neo-animate');
                        }, 100 * index);
                    });

                    // Animate counters
                    const counters = document.querySelectorAll('.number-count');
                    counters.forEach(counter => {
                        const target = +counter.getAttribute('data-target');
                        const duration = 2000; // 2 seconds
                        const increment = target / (duration / 16); // 60fps

                        let currentCount = 0;
                        const updateCount = () => {
                            if (currentCount < target) {
                                currentCount += increment;
                                counter.textContent = Math.min(Math.floor(currentCount), target);
                                requestAnimationFrame(updateCount);
                            } else {
                                counter.textContent = target;
                            }
                        };

                        updateCount();
                    });

                    // Unobserve after animation is triggered
                    statsObserver.unobserve(entry.target);
                }
            });
        }, {
            threshold: 0.2
        });

        // Observe the stats section
        const statsSection = document.querySelector('.stats-showcase');
        if (statsSection) {
            statsObserver.observe(statsSection);
        }

        // Add hover effects for interactive elements
        const metricElements = document.querySelectorAll('.neo-metric');
        metricElements.forEach(metric => {
            metric.addEventListener('mouseenter', function() {
                this.classList.add('neo-hover');
            });
            metric.addEventListener('mouseleave', function() {
                this.classList.remove('neo-hover');
            });
        });

        // Add pulse effect to stars
        const stars = document.querySelectorAll('.neo-star');
        stars.forEach((star, index) => {
            setTimeout(() => {
                star.classList.add('neo-pulse');
            }, 300 * index);
        });
    });
</script>